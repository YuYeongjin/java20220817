package ch06.book.exercise.e16;

public class Printer {
	static void println(int text) {
		System.out.println(text);
	}
	static void println(boolean text) {
		System.out.println(text);
	}
	static void println(double text) {
		System.out.println(text);
	}
	static void println(String text) {
		System.out.println(text);
	}
}
